input.form.addEventListener("submit", function(e) {
  var out;
  e.preventDefault();
  if (input.value.toLowerCase() === current.name.toLowerCase() || input.value.toLowerCase() === current.name.split(' ')[0].toLowerCase()) {
    point.text = ++point.text;
    point.scale = 0.5;
    point.animate({
      properties: {
        scale: 1
      },
      curve: "spring(200,10)"
    });
    peeps = _.without(peeps, current);
    out = card.animate({
      properties: {
        x: Screen.width + 100
      },
      curve: "spring(200,10)"
    });
    out.onAnimationEnd(function() {
      return showNewPerson();
    });
  } else {
    overlay.text = ':( \n\n' + point.text + "/" + peeps.length + "\n\n Click to play again";
    overlay.visible = true;
    point.text = 0;
    peeps = orgPeeps;
    showNewPerson();
  }
  return input.value = "";
});

