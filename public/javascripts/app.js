showNewPerson = function() {
  if (peeps.length === 0) {
    overlay.visible = true;
    overlay.text = "Perfect score! " + point.text + "/" + peeps.length + "\n\nClick to play again";
    return;
  }
  card.x = -600;
  card.rotation = -10;
  current = Utils.randomChoice(peeps);
  card.image = current.img;
  card.scale = 1;
  return card.animate({
    properties: {
      scale: 1,
      midX: Screen.midX,
      midY: Screen.midY,
      rotation: 0
    },
    curve: "spring(200,20)",
    delay: .2
  });
};

